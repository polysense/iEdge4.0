/**
 * @brief LCDÆÁÄ»Çý¶¯ÅÐ¶Ï°´¼ü°´ÏÂÊ±³¤
 * @param[in] p ²ÎÊý¿ØÖÆ¿é
 * @param[in] max_time ×î´ó°´¼üÊ±¼ä
 */
pos_u32_t mtlcd_btn_time_get(mtlcd_param_t *p, pos_u32_t max_time) {
  pos_u32_t ticks, v;
  const pos_lib_t *os;
  os = p->drv.os;

  /* ¼ÇÂ¼ÖÐ¶ÏÊ±¿Ì */
  ticks = p->irq_ticks;  
  p->irq_ticks = 0;

  /* Èç¹ûµ±Ç°°´Å¥Îªµ¯¿ªÔòºöÂÔ±¾´ÎÖÐ¶Ï */
  if( os->gpio->get(p->pin) != 0 ) {
    return 0;
  }

  /* Èç¹û´ÓÎ´ÖÐ¶ÏÔò´Óµ±Ç°Ê±¿ÌÅÐ¶ÏÊ±³¤ */
  if( !ticks )
    ticks = os->tick->get();
  
  /* Èç¹ûÖÐ¶Ï¾à½ñÒÑ¾­ºÜ¾ÃÔò×î¶àÈÏÎªLCDÁÁÆÁÊ±¼ä */
  if( os->tick->elaps(ticks) > p->lcd_time )
    ticks = os->tick->get() - p->lcd_time;

  /* ¼ÆËã³¬Ê± */
  max_time = ticks + max_time;
  
  /* ¼ì²éÖ±µ½×î´óÊ±³¤³¬Ê± */
  while(!os->tick->is_timeout(os->tick->get(), max_time)) {
    /* °´Å¥ËÉ¿ªÁ¢¼´ÍË³ö */
    if( os->gpio->get(p->pin) != 0 ) {
      break;
    }
    /* ÐÝÃß²¢Ö±µ½°´Å¥ËÉ¿ª»òÕß³¬Ê± */
    os->tick->sleep(20);

    v = os->tick->elaps(ticks);
    /* Èç¹û³¬¹ýÁÁÆÁÊ±³¤Ôò¼ì²éÊÇ·ñ¸æ¾¯¹Ø±Õ */
    if( v >= p->lcd_time )
      mtlcd_alarm_set(p, 0);

    /* ´ïµ½É¨ÂëÊ±³¤ºóÆô¶¯ÌáÊ¾Òô */
    if( (p->flags & 2 ) == 0 && v >= p->rfid_time && v < p->rfid_time + 500 ) {
      p->drv.board->beep_set(2700);
      p->flags |= 2;
    /* É¨ÂëÌáÊ¾Òô³¬Ê±¹Ø±Õ */
    } else if( (p->flags & 2) && v >= p->rfid_time + 500 ) {
      p->drv.board->beep_set(0);
      p->flags &= ~2;
    }
    
  }

  /* ¹Ø±ÕÉ¨ÂëÌáÊ¾ */
  if( p->flags & 2 ) {
    p->drv.board->beep_set(0);
    p->flags &= ~2;
  }
  
  /* ·µ»Ø°´Å¥Ê±³¤ */
  return os->tick->elaps(ticks);
}


